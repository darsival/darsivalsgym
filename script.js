window.addEventListener('DOMContentLoaded', () => {
  const langBtn = document.getElementById('langBtn');
  const langMenu = document.getElementById('langMenu');

  if (!langBtn || !langMenu) {
    console.error('Language button or menu not found!');
    return;
  }

  function updateLangBtn(lang) {
    const langMap = {
      en: 'ㅤEN',
      cz: 'ㅤCZ',
      de: 'ㅤDE', 
      es: 'ㅤES',
      fr: 'ㅤFR',
      it: 'ㅤIT',
      pl: 'ㅤPL',
      sk: 'ㅤSK',
      ru: 'ㅤRU',
      ua: 'ㅤUA'
    };
    langBtn.innerHTML = `${langMap[lang] || 'EN'} <svg class="w-4 h-4" fill="none" stroke="currentColor" stroke-width="2" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" d="M19 9l-7 7-7-7"/></svg>`;
  }

  langBtn.addEventListener('click', (e) => {
    e.preventDefault();
    langMenu.classList.toggle('hidden');
  });

  window.addEventListener('click', (e) => {
    if (!langBtn.contains(e.target) && !langMenu.contains(e.target)) {
      langMenu.classList.add('hidden');
    }
  });

  langMenu.querySelectorAll('a').forEach(link => {
    link.addEventListener('click', (e) => {
      e.preventDefault();
      const href = link.getAttribute('href');
      const urlParams = new URLSearchParams(href.split('?')[1]);
      const selectedLang = urlParams.get('lang');

      if (!selectedLang) return;

      localStorage.setItem('siteLanguage', selectedLang);

      updateLangBtn(selectedLang);

      const url = new URL(window.location);
      url.searchParams.set('lang', selectedLang);
      window.location = url.toString();
    });
  });

  const urlLang = new URL(window.location).searchParams.get('lang');
  const storedLang = localStorage.getItem('siteLanguage');

  if (urlLang) {
    updateLangBtn(urlLang);
    localStorage.setItem('siteLanguage', urlLang);
  } else if (storedLang) {
    updateLangBtn(storedLang);
  } else {
    updateLangBtn('en'); 
  }
});





